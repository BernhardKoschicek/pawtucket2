		<h1>José Ignacio Tellechea Idigoras</h1>
		<article id="infoLibro" class="alignLeft">
			<p class="mini">
			Eclesiástico, historiador y profesor nacido en Donostia-San Sebastián
			(Gipuzkoa) el 13 de abril de 1928. Falleció en Donostia-San Sebastián
			el 8 de marzo de 2008 a los 79 años de edad.
			</p>
			
			<figure class="alignLeft"><?php print caGetThemeGraphic($this->request, "books/tellechea_1.jpg", array("alt" => "José Ignacio Tellechea Idígoras")); ?><p><i>José Ignacio Tellechea Idígoras</i></p></figure>
			
			<p class="mini">
			Cursó sus estudios sacerdotales en el Seminario Menor de Bergara y en
			el Mayor de Vitoria-Gasteiz (1940-1951), ordenándose sacerdote el 29
			de junio de 1951. Becario del Instituto Español de Estudios
			Eclesiásticos de Roma (1954-1956), amplió allí sus estudios
			licenciándose en Historia de la Iglesia en la Universidad Gregoriana
			(1955) y diplomándose en la Escuela Vaticana de Archivística y
			Paleografía (1955). Su tesis, sobre La Inmaculada Concepción en la
			controversia del P. Maldonado... con la Sorbona, obtuvo la medalla de
			oro de la Gregoriana, siendo publicado en "Victoriensia" en 1958. Años
			más tarde fue becario de la Fundación Juan March (1958-1966) y obtuvo
			la licenciatura con premio extraordinario en Filosofía y Letras,
			Historia, en la Universidad Complutense de Madrid (1966).
			</p>
			
			<p class="mini">
			Fue profesor de Teología Fundamental y de Historia de la Iglesia,
			además de bibliotecario, en el Seminario de Donostia-San Sebastián
			(1956-1970) y rector del mismo entre 1966 y 1968. Ocupaciones que
			compaginó con la docencia en el Seminario Hispano-Americano de Madrid
			(1956-1966) y con el cargo de Asesor Conciliar del Episcopado Español
			(1963-1966).
			</p>
			
			<p class="mini">
			En 1966 obtuvo la cátedra de Historia de la Iglesia de la Universidad
			Pontificia de Salamanca, cargo que ha desempeñado hasta su jubilación
			(1998), siendo Catedrático Emérito de la misma. Durante su etapa
			salmantina también conjugó su actividad docente con otras iniciativas,
			siendo fundador y director del Instituto Pío XII de Teología para
			Religiosas de Donostia-San Sebastián (1967-1998) e idénticas funciones
			tuvo respecto al Instituto Dr. Camino de Historia de esta ciudad
			(1967-2000), además, entre 1972 y 1975 dirigió la revista
			"Salmanticensis". Su intensa vida como historiador e investigador se
			ha ido enriqueciendo con diversas distinciones, siendo miembro
			numerario de la Real Sociedad Bascongada de Amigos del País (RSBAP)
			(1966); correspondiente de la Real Academia Española de Historia, así
			como de las de Venezuela y Méjico; miembro de Honor del "Institut de
			l'Humanisme et la Renaissance" de Tours y del Instituto
			Vasco-Argentino de Buenos Aires; miembro del Patronato de la Fundación
			Universitaria Española; y director de Espirituales Españoles.
			</p>
			<figure class="alignRight aireLeft"><?php print caGetThemeGraphic($this->request, "books/tellechea_2.jpg", array("alt" => "José Ignacio Tellechea Idígoras")); ?><p><i>José Ignacio Tellechea Idígoras</i></p></figure>
			
			<p class="mini">
			Conferenciante y articulista, colaborador de "El Diario Vasco" durante
			muchos años, ha participado en congresos y reuniones científicas en
			lugares tales como Oxford, París, Roma, Salamanca, Madrid, Trento,
			Tours, Ginebra o Sevilla, totalizando su obra más de 100 libros e
			incontables artículos; con una labor divulgativa que supera el millar
			de artículos. Colaborador, entre otras muchas publicaciones, del
			"Bulletin Hispanique", "Hispania Sacra", "Boletín de la Real Academia
			de la Historia", "Revista Española de Teología", "Boletín de la
			RSBAP", "Príncipe de Viana" y "Boletín de Estudios Históricos sobre
			San Sebastián". Comenzó en los años 50 el estudio sobre la vida y obra
			de fray Bartolomé Carranza, al que ha dedicado una parte importante de
			su labor historiográfica, fruto de la cual son las obras: El Arzobispo
			Carranza y su tiempo (Madrid, 1968); Bartolomé Carranza. Comentarios a
			su "Cathechismo Cristiano" (Madrid, 1972); Fray Bartolomé Carranza y
			el cardenal Pole. Un navarro en la restauración católica en Inglaterra
			1554-1558 (Pamplona, 1977); y Melanchton y Carranza (Salamanca, 1979),
			habiendo publicado asimismo numerosos artículos sobre el mismo
			personaje y 7 volúmenes de documentación sobre dicho tema 1962 y 1983
			(Archivo Documental Español). Otra figura a la que dedicó largos
			estudios fue el P. Larramendi, destacando la edición de sus obras
			Corografía de Guipúzcoa (San Sebastián, 1969); Autobiografía y otros
			escritos, (San Sebastián, 1973) y Conferencias... sobre los Fueros
			(San Sebastián, 1983).
			</p>
			
			<p class="mini">
			Autor también de La Reforma Tridentina en San Sebastián (San
			Sebastián, 1970 y 1972); las ediciones de Las 110 divinas
			consideraciones de Juan de Valdés (Salamanca, 1975), la Guía
			Espiritual de Miguel de Molinos (Madrid, 1976); Papeles Viejos (San
			Sebastián, 1968); Tiempos Recios. Inquisición y Heterodoxias, con
			prólogo de M. Bataillon (Salamanca, 1977); Anclas de Hernani (San
			Sebastián, 1977); Hernán Pérez de Yarza... Las Comunidades y la guerra
			de Navarra 1520-1521 (San Sebastián, 1979); Erquicia y Aozaraza (San
			Sebastián, 1980); Ignace de Loyola (París, 1983); Bibliografía de la
			RSBAP (San Sebastián, 1985); Arte en el Antiguo Aranzazu del siglo
			XVIII (San Sebastián, 1987); edición de las Cartas del conde de
			Peñaflorida a P. Jacinto de Alava, La Ilustración Vasca (Vitoria,
			1987); y La Otra Cara de la Invencible. La participación vasca (San
			Sebastián, 1988). Miguel de Unamuno (5 libros); Felipe II; La monja
			Alférez. Doña Catalina de Erauso; Dario de Regoyos. Cartas. Una
			bibliografía del P. Tellechea se puede consultar en el "Homenaje a
			J. I. Tellechea" que contienen los volúmenes 16 y 17 (1982-1983) del
			"Boletín de Estudios Históricos sobre San Sebastián" editado por Kutxa
			Fundazioa.
			</p>
			
