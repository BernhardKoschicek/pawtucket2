                        <p>The Board of Trustees and staff of The Noguchi Museum are pleased to present The Isamu Noguchi Catalogue Raisonné. This digital publication is an ongoing project of The Isamu Noguchi Foundation and Garden Museum, New 	York. The Isamu Noguchi Catalogue Raisonné is dedicated to documenting the artistic practice of Isamu Noguchi (1904–1988), and the Museum ensures its access is made freely available to scholars and the public.</p>
						<p>The history of The Isamu Noguchi Catalogue Raisonné dates to the mid-1970s, when Isamu Noguchi was still working actively in his Long Island City studio. It began with research and documentation of Noguchi’s sculptural practice that resulted in the publication of The Sculpture of Isamu Noguchi, 1904–1979: A Catalogue (Garland, 1980), by Nancy Grove and Diane Botnick. For many years, this volume was the most comprehensive resource on the artist’s practice, even though Noguchi went on to make artworks for another nine years after its publication.</p>
						<p>Several decades of research and documentation support the current project, which updates and considerably expands upon the 1980 catalogue. Throughout the eighties, Noguchi’s studio staff established a systematic inventory program for both new and undocumented artworks. Upon the artist’s death in 1988, the importance of developing a new more comprehensive catalogue raisonné became far more apparent. In subsequent years, inventory efforts continued alongside new projects that documented Noguchi’s studios in New York, Japan, and Italy.</p>
						<p>By the early 1990s, those who had worked most closely with Noguchi assumed new roles within The Isamu Noguchi Foundation, including Shoji Sadao as Director, Bonnie Rychlak as Registrar, and Amy Hau as Archivist. During this decade, the appraisal of the artist’s estate led to the documentation of previously unrecorded artworks, and the processing of Noguchi’s archives was begun. Increased awareness of the depth of the estate’s historical documents and photographs, together with the growing studio and estate inventories, made clearer than ever the need to create a comprehensive catalogue raisonné. The Foundation’s Board supported this new project, which continued throughout the decade with a new concentration on recording artworks outside of the Foundation’s collection. Toward the end of the 1990s, Neil Printz was hired as the first Catalogue Raisonné Editor, working closely with Bonnie Rychlak, who assumed the role of Managing Editor when he left the project.</p>
